import math

# Rounds a number up to the nearest integer.
print(math.ceil(10 / 3))

# Rounds a number down to the nearest integer.
print(math.floor(10 / 3))

# Writes as 5 to the power 3
print(math.pow(13, 0.5))

# Writes as square root of 1024
print(math.sqrt(1024))

num = 13
print(int(num ** 0.5))
